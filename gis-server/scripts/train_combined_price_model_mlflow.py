def load_source_weights(weights_path: Path | None) -> dict[str, float]:
    weights = DEFAULT_SOURCE_WEIGHTS.copy()
    if weights_path is None:
        return weights
    payload = json.loads(weights_path.read_text(encoding="utf-8"))
    for key, value in payload.items():
        weights[str(key)] = float(value)
    return weights


def build_sample_weights(
    df: pd.DataFrame, source_weights: dict[str, float] | None
) -> np.ndarray | None:
    if source_weights is None or len(source_weights) == 0:
        return None
    weights = df["source_site"].map(
        lambda value: float(source_weights.get(str(value), 1.0))
    )
    return weights.to_numpy(dtype=float)


def build_category_maps(dfs: Sequence[pd.DataFrame]) -> dict[str, dict[str, int]]:
    category_maps: dict[str, dict[str, int]] = {}
    for column in CATEGORICAL_COLUMNS:
        values: set[str] = set()
        for df in dfs:
            if column not in df.columns:
                continue
            values.update(df[column].fillna("unknown").astype(str).unique().tolist())
        ordered = sorted(values)
        category_maps[column] = {value: index for index, value in enumerate(ordered)}
    return category_maps


def encode_categoricals(
    df: pd.DataFrame, category_maps: dict[str, dict[str, int]] | None = None
) -> pd.DataFrame:
    result = df.copy()
    result["source_type_is_listing"] = (result["source_type"] == "listing").astype(int)

    maps = category_maps or build_category_maps([result])
    for column in CATEGORICAL_COLUMNS:
        mapping = maps[column]
        normalized = result[column].fillna("unknown").astype(str)
        fallback = len(mapping)
        result[f"{column}_encoded"] = normalized.map(
            lambda value: mapping.get(value, fallback)
        ).astype(int)
    return result


